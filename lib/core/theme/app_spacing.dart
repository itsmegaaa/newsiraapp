/// Defines spacing tokens to maintain consistent padding and margin
/// throughout the UI. All values are multiples of 4.0 as per the
/// design system guidelines. Avoid arbitrary numbers such as 7 or
/// 18—choose the closest token instead.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 12.0;
  static const double base = 16.0;
  static const double lg = 20.0;
  static const double xl = 24.0;
  static const double xxl = 32.0;
  static const double xxxl = 40.0;
}